package dto;

public class Hashtag {
	private String tag_id;
	private long post_id;
	
	public String getTag_id() {
		return tag_id;
	}
	public void setTag_id(String tag_id) {
		this.tag_id = tag_id;
	}
	public long getPost_id() {
		return post_id;
	}
	public void setPost_id(long result) {
		this.post_id = result;
	}
}
